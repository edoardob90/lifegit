"""Story narratives for each act"""

ACT1_NARRATIVE = {
    "introduction": """You're 18. High school is over.

Your friends are scattered—some to university, some to work, some
traveling. The question everyone keeps asking: "What's next for you?"

You've been thinking about it for weeks. Maybe months. The options
spin in your head: university, a gap year, starting work, learning
a trade, traveling... Each path leads somewhere different. Each
choice closes other doors.

This is your first major decision. The kind that shapes everything
that comes after.

Notice that feeling? That slight anxiety, the weight of unmade
choices? That's what uncommitted changes feel like in git—and in life.
Files sitting in your working directory, modified but not saved to
history. Options open, but no forward momentum.""",
    "conclusion": """You've committed. The decision is made.

Notice how that felt? There's a strange relief in commitment, isn't
there? The anxiety of options gives way to the clarity of direction.

Derek Sivers writes about this: "To go one direction means you're
not going other directions." The act of committing—in git and in
life—cuts off possibilities. But it also creates forward momentum.
You can finally move.

Your first commit is now part of your permanent history. You can
build on it, branch from it, even eventually change course. But
you can never un-decide. The act of deciding is irreversible, even
when the outcome isn't.

In git terms:
  Working directory = option paralysis (anxiety)
  git commit       = making the decision (relief, clarity)

You've taken your first step. Your life repository has begun.""",
}

ACT2_NARRATIVE = {
    "introduction": """A few years have passed. You're in your mid-twenties now.

The path you chose in Act 1 has led you here—wherever 'here' is for
you. But lately, you've been wondering: What if?

What if you'd chosen differently back then? What if there's still
another path worth exploring? The road not taken still whispers
sometimes, especially late at night.

Here's the thing about life: you can't actually go back and try the
other path. But in git? You can. That's what branches are for.

Branches let you explore alternate timelines without destroying your
main story. You can ask "what if?" safely, knowing your original path
is preserved. This is one of git's most powerful features—and one of
the healthiest ways to think about life choices.""",
    "conclusion": """You've created alternate timelines.

Notice something important: your main branch is still there, unchanged.
The "what if" branches didn't destroy anything—they added possibilities.

This is the beauty of branching. In git, and perhaps in life's thought
experiments, exploring alternatives doesn't mean abandoning your path.
You can hold multiple possibilities, examine them, and then choose
which to pursue.

Some branches you'll merge back (we'll do that in Act 3). Some you'll
let go. But the act of exploring them—of giving yourself permission
to wonder "what if?"—is valuable in itself.

In git terms:
  git branch   = asking "what if?"
  git checkout = stepping into that alternate timeline
  main branch  = your actual path, always preserved

You now have the power to explore without fear of losing your way.""",
}
